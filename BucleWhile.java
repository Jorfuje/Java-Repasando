public class BucleWhile {
    public static void main(String[] args) {

        // Declaraci√≥n de variable i
        int i = 0;
        // Bucle while que se ejecuta mientras i sea menor que 10
        while (i < 10) {
            // Imprime el valor de i
            System.out.println("i = " + i);
            // Incrementa el valor de i
            i++;
        }

        // Imprime un mensaje al finalizar el bucle
        System.out.println("Fin del bucle while" + "valor de i = " + i);
    }
}
